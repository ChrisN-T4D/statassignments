import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { statisticsExercises } from '../src/data/statisticsPractices.js'
import { allStatisticsQuestions } from '../src/data/conceptQuestions.js'
import { CONCEPT_QUESTION_SOFTWARE_PATCHES } from '../src/data/conceptQuestionSoftware.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const reportPath = path.join(root, 'docs', 'PARITY_REPORT.md')
const exceptionsPath = path.join(root, 'scripts', 'software-parity-exceptions.json')
const SOFTWARES = ['jamovi', 'r', 'spss', 'excel', 'stata']

const exceptions = fs.existsSync(exceptionsPath)
  ? JSON.parse(fs.readFileSync(exceptionsPath, 'utf8'))
  : { allowedMissingSoftwareByKey: {} }

const keyed = new Map()
for (const ex of statisticsExercises) {
  if (!ex.practiceObjectiveKey) continue
  if (!keyed.has(ex.practiceObjectiveKey)) keyed.set(ex.practiceObjectiveKey, [])
  keyed.get(ex.practiceObjectiveKey).push(ex)
}

const rows = []
let failing = 0
for (const [key, items] of [...keyed.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
  const present = new Set(items.map((x) => x.software_type))
  const allowedMissing = new Set(exceptions.allowedMissingSoftwareByKey?.[key] || [])
  const missing = SOFTWARES.filter((s) => !present.has(s))
  const actionableMissing = missing.filter((s) => !allowedMissing.has(s))
  if (actionableMissing.length > 0) failing += 1
  const module = items[0].module
  rows.push({
    key,
    module,
    present: SOFTWARES.filter((s) => present.has(s)).join(', ') || '-',
    missing: missing.join(', ') || '-',
    allowedMissing: [...allowedMissing].join(', ') || '-',
    status: actionableMissing.length ? `FAIL: missing ${actionableMissing.join(', ')}` : 'PASS'
  })
}

const patchIds = new Set(Object.keys(CONCEPT_QUESTION_SOFTWARE_PATCHES))
const mentionsSoftware = allStatisticsQuestions.filter((q) =>
  /jamovi|spss|stata|excel|rstudio|\bLN\(|\bLOG10\(|\.omv|logical operator|compute function/i.test(
    [
      q.question,
      q.hint,
      q.feedback?.correct,
      q.feedback?.incorrect,
      ...(q.options || []).map((o) => o.text)
    ]
      .filter(Boolean)
      .join(' ')
  )
)
const mentionNotPatched = mentionsSoftware.filter((q) => !patchIds.has(q.id))

const lines = []
lines.push('# Software Parity Report')
lines.push('')
lines.push(`- Objective keys checked (keyed): **${rows.length}**`)
lines.push(`- Practice parity failures: **${failing}**`)
lines.push(`- Concept items with software signals: **${mentionsSoftware.length}**`)
lines.push(`- Concept items with software signals but no patch: **${mentionNotPatched.length}**`)
lines.push('')
lines.push('## Practice objective parity')
lines.push('')
lines.push('| Key | Module | Present | Missing | Allowed missing | Status |')
lines.push('|---|---|---|---|---|---|')
for (const r of rows) {
  lines.push(`| \`${r.key}\` | \`${r.module}\` | ${r.present} | ${r.missing} | ${r.allowedMissing} | ${r.status} |`)
}
lines.push('')
lines.push('## Concept patch backlog (software-signaled, unpatched)')
lines.push('')
for (const q of mentionNotPatched) {
  lines.push(`- \`${q.id}\`: ${q.question}`)
}
lines.push('')
lines.push(`Generated by \`scripts/verify-practice-parity.mjs\`.`)
lines.push('')

fs.writeFileSync(reportPath, `${lines.join('\n')}\n`, 'utf8')
console.log(`Wrote ${reportPath}`)
if (failing > 0) {
  console.error(`Practice parity failures: ${failing}`)
  process.exitCode = 2
}
