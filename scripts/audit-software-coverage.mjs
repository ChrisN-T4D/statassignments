/**
 * Reports software lesson coverage per stats-module and topic HTML completeness.
 * Read-only; prints to stdout and writes docs/SOFTWARE_COVERAGE.md
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { softwareLessons, softwareModules } from '../src/data/softwareLessons.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const topicsDir = path.join(root, 'src', 'content', 'topics')
const OUT = path.join(root, 'docs', 'SOFTWARE_COVERAGE.md')

const SOFTWARES = ['jamovi', 'spss', 'r', 'excel', 'stata']
const STATS_PREFIX = 'stats-module-'

function countLessonsByModuleSoftware() {
  const map = new Map()
  for (const lesson of softwareLessons) {
    if (!lesson.module?.startsWith(STATS_PREFIX)) continue
    const k = lesson.module
    if (!map.has(k)) map.set(k, { jamovi: 0, spss: 0, r: 0, excel: 0, stata: 0 })
    const sw = lesson.software
    if (map.get(k)[sw] != null) map.get(k)[sw] += 1
  }
  return map
}

function scanTopicHtml() {
  const bySlug = new Map()
  if (!fs.existsSync(topicsDir)) return bySlug
  const slugs = fs.readdirSync(topicsDir, { withFileTypes: true }).filter(d => d.isDirectory())
  for (const d of slugs) {
    const slug = d.name
    const dir = path.join(topicsDir, slug)
    const files = fs.readdirSync(dir).filter(f => f.endsWith('.html'))
    const present = new Set(files.map(f => f.replace(/\.html$/u, '')))
    bySlug.set(slug, present)
  }
  return bySlug
}

function statsModuleTopicSlugs(bySlug) {
  return [...bySlug.keys()].filter(s => s.includes('__module-') && !s.startsWith('rm-'))
}

function main() {
  const lessonCounts = countLessonsByModuleSoftware()
  const bySlug = scanTopicHtml()
  const statSlugs = statsModuleTopicSlugs(bySlug)

  let topicIncomplete = 0
  const topicRows = []
  for (const slug of statSlugs.sort()) {
    const present = bySlug.get(slug) || new Set()
    const missing = SOFTWARES.filter(s => !present.has(s))
    if (missing.length) topicIncomplete += 1
    topicRows.push({
      slug,
      missing: missing.length ? missing.join(', ') : '-'
    })
  }

  const lines = []
  lines.push('# Software coverage audit')
  lines.push('')
  lines.push(`Generated by \`npm run audit:software\`.`)
  lines.push('')
  lines.push('## Statistics software lessons (by module)')
  lines.push('')
  lines.push('| Module | jamovi | spss | r | excel | stata |')
  lines.push('|--------|--------|------|---|-------|-------|')
  for (const mod of softwareModules) {
    const c = lessonCounts.get(mod.id) || { jamovi: 0, spss: 0, r: 0, excel: 0, stata: 0 }
    lines.push(
      `| ${mod.id} | ${c.jamovi} | ${c.spss} | ${c.r} | ${c.excel} | ${c.stata} |`
    )
  }
  lines.push('')
  lines.push('## Statistics topic HTML (per slug)')
  lines.push('')
  lines.push(`- Topic slugs under \`src/content/topics/*__module-*\` (excl. rm-*): **${statSlugs.length}**`)
  lines.push(`- Slugs missing at least one of [${SOFTWARES.join(', ')}]: **${topicIncomplete}**`)
  lines.push('')
  lines.push('| Slug | Missing HTML |')
  lines.push('|------|----------------|')
  for (const r of topicRows) {
    lines.push(`| \`${r.slug}\` | ${r.missing} |`)
  }
  lines.push('')

  fs.writeFileSync(OUT, `${lines.join('\n')}\n`, 'utf8')
  console.log(`Wrote ${OUT}`)
  console.log('Lesson counts by stats-module (jamovi/spss/r/excel/stata):')
  for (const mod of softwareModules) {
    const c = lessonCounts.get(mod.id) || {}
    console.log(`  ${mod.id}:`, c)
  }
  console.log(`Topic slugs with any missing software HTML: ${topicIncomplete}`)
}

main()
