<template>
  <div class="concept">
    <p>
      Data visualization is essential in psychology research for exploring your data,
      checking assumptions, and communicating findings. Different types of graphs are
      suited for different purposes.
    </p>

    <h3>Histograms</h3>
    <p>
      Histograms show the distribution of a single continuous variable. They help you see:
    </p>
    <ul>
      <li>The shape of your distribution (normal, skewed, bimodal)</li>
      <li>The central tendency and spread</li>
      <li>Potential outliers</li>
    </ul>

    <div class="tip">
      <div class="tip-title">Reading a Histogram</div>
      <p>
        The x-axis shows ranges of values (bins), and the y-axis shows frequency or count.
        A bell-shaped histogram suggests normally distributed data.
      </p>
    </div>

    <h3>Box Plots (Box-and-Whisker Plots)</h3>
    <p>
      Box plots display the five-number summary: minimum, Q1, median, Q3, and maximum.
      They're excellent for:
    </p>
    <ul>
      <li>Comparing distributions across groups</li>
      <li>Identifying outliers (shown as individual points)</li>
      <li>Seeing the spread and skewness at a glance</li>
    </ul>

    <div class="note">
      <div class="note-title">Anatomy of a Box Plot</div>
      <p>
        <strong>Box:</strong> Shows the interquartile range (IQR) from Q1 to Q3<br>
        <strong>Line in box:</strong> The median (50th percentile)<br>
        <strong>Whiskers:</strong> Extend to min/max within 1.5 × IQR<br>
        <strong>Points beyond whiskers:</strong> Potential outliers
      </p>
    </div>

    <h3>Scatter Plots</h3>
    <p>
      Scatter plots show the relationship between two continuous variables. Each point
      represents one participant's scores on both variables.
    </p>
    <ul>
      <li><strong>Positive relationship:</strong> Points trend upward (as X increases, Y increases)</li>
      <li><strong>Negative relationship:</strong> Points trend downward (as X increases, Y decreases)</li>
      <li><strong>No relationship:</strong> Points show no clear pattern</li>
    </ul>

    <h3>Bar Charts</h3>
    <p>
      Bar charts display frequencies or means for categorical variables. Use them when:
    </p>
    <ul>
      <li>Comparing means across groups</li>
      <li>Showing frequencies of categorical responses</li>
      <li>Displaying results of experiments with discrete conditions</li>
    </ul>

    <div class="tip">
      <div class="tip-title">Bar Chart vs. Histogram</div>
      <p>
        <strong>Histograms</strong> are for continuous data—bars touch each other.<br>
        <strong>Bar charts</strong> are for categorical data—bars have gaps between them.
      </p>
    </div>

    <h3>APA Style Guidelines</h3>
    <p>When creating figures for psychology papers:</p>
    <ul>
      <li>Include clear axis labels with units</li>
      <li>Use a figure number and descriptive caption</li>
      <li>Avoid 3D effects and unnecessary decoration</li>
      <li>Include error bars when showing means</li>
    </ul>
  </div>
